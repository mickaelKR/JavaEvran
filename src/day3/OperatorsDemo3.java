package day3;

public class OperatorsDemo3 {
	
	public static void main (String [ ] args) {
		
		
		// 3. Increment/Decrement Operators
		// ++/--
		// post/pre
		// a++ a-- / ++a --a
		
		int a = 5;
		System.out.println(a);
		a++;// a = a +1;
		System.out.println(a);
		
		int x = 7;
		System.out.println(x); //7
		System.out.println(x++);//7
		System.out.println(++x);//9
		System.out.println(--x);//8
		
		
		
		
		
		
		
		
		
		
	}
		
	

}
